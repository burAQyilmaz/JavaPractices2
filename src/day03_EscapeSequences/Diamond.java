public class Diamond {
    public static void main(String[] args) {

        /*
        Create a class called Diamond.java, and print:

                    ^
                   / \
                  /   \
                 /     \
                /       \
                ---------
                \       /
                 \     /
                  \   /
                   \ /
                    V
         */

        System.out.println("\t\t\t     ^");
        System.out.println("\t\t\t    / \\");
        System.out.println("\t\t\t   /   \\");
        System.out.println("\t\t\t  /     \\");
        System.out.println("\t\t\t /       \\");
        System.out.println("\t\t\t ---------");
        System.out.println("\t\t\t \\       /");
        System.out.println("\t\t\t  \\     /");
        System.out.println("\t\t\t   \\   /");
        System.out.println("\t\t\t    \\ /");
        System.out.println("\t\t\t     V");


    }
}

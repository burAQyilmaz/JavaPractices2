package day03_EscapeSequences;

public class EscapeSequences {

    /*
       \n: starts a new line
       \t: paragraph space(tab)
       \\: used for print single backslash
       \": double quote

        */


    public static void main(String[] args) {


        System.out.println("Java \nPython \nC#");

        System.out.println("------------------------------------------");

        System.out.println("Hello everyone? \nhow are you all today? \nToday we will learn Escape Sequences, \nand next week we will learn vriables\"");

        System.out.println("------------------------------------------");

        System.out.println("\tJava is a cool programming language");

        System.out.println("------------------------------------------");

        System.out.println("\\");

        System.out.println("------------------------------------------");

        System.out.println("My favourite film is \"Gofather\".");

    }
}
